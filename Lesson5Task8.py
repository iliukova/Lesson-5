# Task 8
# Створіть список випадкових чисел (розміром 4 елементи).
# Створіть другий список у два рази більше першого, де перші 4 елементи повинні
# дорівнювати елементам першого списку, а решта елементів - подвоєним значенням початкових.
# Наприклад,
# Було → [1,4,7,2]
# Стало → [1,4,7,2,2,8,14,4]

A = 4
import random
a = []
v = []
for i in range(A):
    x = random.randint(0, 100)
    a.append(x)
    b = a[i]*2
    v.append(b)
print(a)
print(a+v)
